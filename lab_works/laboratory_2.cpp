/* Лабораторна робота № 2
Варіант No 2.
Умова:
Знайти значення функції F на трьох інтервалах:
1. Обрати графік функцій, згідно свого варіанту. Записати математичне вираження для функції F на трьох інтервалах.
2. Скласти блок-схему та написати програму, яка визначає значення функції F для введеного з клавіатури числа х.
                                                                        ***
Що робить ця програма/цей код:
Ця програма запитує користувача значення x, обчислює значення функції F(x) за допомогою умовного оператора if,
відповідного математичному виразу для функції, і виводить результат на екран.
В даному випадку використано строгу нерівність <= для першого інтервалу та < для другого інтервалу, щоб враховувати межі інтервалів.
                                                                        ***
Покроковий опис роботи коду/вирішення задачі:
1. Спочатку необхідно описати функцію, яка відповідає заданому графіку. 
    З опису графіка випливає, що функція складається з трьох інтервалів, кожному з яких задано своє значення. 
    Тому функцію можна описати за допомогою умовного оператора if, який дозволяє встановити значення функції на кожному інтервалі.
    Таким чином, вираз функції може бути записано наступним чином:
    f(x) = if(x <= -1, a, if(x < 1, x^2, b))
    де a і b - константи, що визначають значення функції першому і третьому інтервалах відповідно, 
    але в другому інтервалі функція дорівнює y = x^2.
2. Потім необхідно написати програму, яка дозволяє визначити значення функції для заданого значення x. 
    Для цього можна використовувати оператор умовного виразу if, який дозволяє перевірити, на якому інтервалі 
    знаходиться значення x і задати відповідне значення функції.
3. Для початку програми необхідно оголосити змінні зберігання значення x, констант a і b, а також змінну для зберігання значення функції.
4. Потім потрібно запитати у користувача значення x. Для цього можна використовувати оператор cin.
5. Далі необхідно використовувати умовний оператор if для обчислення значення функції залежно від x. 
    Спочатку перевіряємо, чи значення x на першому інтервалі, і якщо так, то задаємо значення функції рівним a. 
    Якщо ні, то перевіряємо, чи значення x на другому інтервалі, і якщо так, то задаємо значення функції рівним x^2. 
    І, нарешті, якщо значення x перебуває в третьому інтервалі, то задаємо значення функції рівним b.

    *** Константи -2 і 3 були обрані для прикладу і можуть бути замінені будь-якими іншими значеннями
        відповідно до точних значень графіка функції на першому та третьому інтервалах. ***

    *** в даному випадку використана сувора нерівність `<= ***
*/

#include <iostream>
#include <cmath>

using namespace std;

int main()
{
    double x, a, b, result;
    cout << "Enter x: ";
    cin >> x;
    if (x <= -1)
    {
        a = -2; // значение функции на первом интервале
        result = a;
    }
    else if (x < 1)
    {
        result = pow(x, 2); // значение функции на втором интервале
    }
    else
    {
        b = 3; // значение функции на третьем интервале
        result = b;
    }
    cout << "F(x) = " << result << endl;
    return 0;
}
